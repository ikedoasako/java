//equalsメソッド

import java.util.*;

public class test7 {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in); /*標準入力から受け取った文字列*/
        String name = scan.next(); /*受け取った文字列を変数nameに代入し*/
        System.out.println("Hello " + name); /*”Hello”という文字列とnameを連結させて表示させる*/
    }
}
